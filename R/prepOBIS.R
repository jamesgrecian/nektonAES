######################################
### Prep the dates of OBIS objects ###
######################################

# this is for pleuragramma obis data

prepOBIS <- function(dat){
  
  dat <- dat |>
  mutate(eventDate = case_when(eventDate == "30.04.2011" ~ "2011-04-30 00:00:00",
                               eventDate == "01.05.2011" ~ "2011-05-01 00:00:00",
                               eventDate == "02.05.2011" ~ "2011-05-02 00:00:00",
                               eventDate == "03.05.2011" ~ "2011-05-03 00:00:00",
                               eventDate == "04.05.2011" ~ "2011-05-04 00:00:00",
                               eventDate == "05.05.2011" ~ "2011-05-05 00:00:00",
                               eventDate == "06.05.2011" ~ "2011-05-06 00:00:00",
                               eventDate == "07.05.2011" ~ "2011-05-07 00:00:00",
                               eventDate == "08.05.2011" ~ "2011-05-08 00:00:00",
                               eventDate == "09.05.2011" ~ "2011-05-09 00:00:00",
                               eventDate == "11.05.2011" ~ "2011-05-11 00:00:00",
                               eventDate == "12.05.2011" ~ "2011-05-12 00:00:00",
                               eventDate == "13.05.2011" ~ "2011-05-13 00:00:00",
                               eventDate == "15.05.2011" ~ "2011-05-15 00:00:00",
                               eventDate == "1989-02-00" ~ "1989-02-01 00:00:00",
                               eventDate == "1997-11-00" ~ "1997-11-01 00:00:00",
                               eventDate == "1985-01-00" ~ "1985-01-01 00:00:00",
                               eventDate == "1989-01-00" ~ "1989-01-01 00:00:00",
                               eventDate == "1989-09-30 / 1992-03-31" ~ "1992-03-31 00:00:00",
                               eventDate == "2007-12/ 2008-01" ~ "2007-12-01 00:00:00",
                               eventDate == "1991-00-00" ~ NA,           
                               eventDate == "0000-00-00" ~ NA,
                               eventDate == "1904-00-00" ~ NA,
                               .default = as.character(eventDate)))
  
  return(dat)
}

